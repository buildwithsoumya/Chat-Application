const CATEGORY_LABELS = ["😀", "👋", "🐶", "🍎", "⚽", "🚗", "💡", "❤️"]

interface EmojiPickerProps {
  onSelect: (emoji: string) => void
  onClose: () => void
}

export function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(0)
  const pickerRef = useRef<HTMLDivElement>(null)
  const categoryNames = Object.keys(EMOJI_CATEGORIES)

  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (pickerRef.current && !pickerRef.current.contains(e.target as Node)) {
        onClose()
      }
    }
    document.addEventListener("mousedown", handleClickOutside)
    return () => document.removeEventListener("mousedown", handleClickOutside)
  }, [onClose])

  return (
    <div
      ref={pickerRef}
      className="absolute bottom-full right-0 mb-2 w-80 glass-strong rounded-2xl border border-border/50 shadow-2xl overflow-hidden animate-scale-in z-50"
    >
      {/* Emoji grid */}
      <div className="h-56 overflow-y-auto custom-scrollbar p-2">
        <div className="grid grid-cols-8 gap-0.5">
          {(EMOJI_CATEGORIES as Record<string, string[]>)[categoryNames[activeCategory]].map((emoji, i) => (
            <button
              key={`${emoji}-${i}`}
              type="button"
              onClick={() => onSelect(emoji)}
              className="h-9 w-9 flex items-center justify-center rounded-lg hover:bg-white/[0.08] active:scale-90 transition-all text-xl"
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>

      {/* Category tabs */}
      <div className="flex items-center justify-around border-t border-border/30 p-1.5">
        {CATEGORY_LABELS.map((label, i) => (
          <button
            key={i}
            type="button"
            onClick={() => setActiveCategory(i)}
            className={cn(
              "h-8 w-8 flex items-center justify-center rounded-lg text-lg transition-all",
              activeCategory === i
                ? "bg-white/[0.08] scale-110"
                : "opacity-60 hover:opacity-100"
            )}
          >
            {label}
          </button>
        ))}
      </div>
    </div>
  )
}
